#greaterThan Function
def greaterThan(x,y):
    if x > y:
        return True

    else:
        return False

#lessThan Function
def lessThan(x,y):
    if x < y:
        return True
    
    else:
        return False

#equalTo Function
def equalTo(x,y):
    if x == y:
        return True
    
    else:
        return False

#greaterOrEqualTo Function
def greaterOrEquao(x,y):
    if x >= y:
        return True

    else:
        return False

#lessThanOrEqualTo Function
def lessThanOrEqual(x,y):
    if x <= y:
        return True

    else:
        return False
